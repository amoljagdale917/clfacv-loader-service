CREATE TABLE STG_HK_OBS_FACVDW (
    STG_KEY      NUMBER NOT NULL,
    CUST_ACCT_NO NUMBER(15),
    SYS_COD      VARCHAR2(3 CHAR),
    ITL_CUST_NO  VARCHAR2(11 CHAR),
    CUST_ID      NUMBER(11),
    FILLER1      VARCHAR2(7 CHAR),
    REC_TYPE     VARCHAR2(1 CHAR),
    TREE_ID      VARCHAR2(24 CHAR),
    LMT_ID       VARCHAR2(5 CHAR),
    MAINT_ACT    VARCHAR2(1 CHAR),
    CRTD_DT_TM   TIMESTAMP(6) NOT NULL,
    CRTD_BY      VARCHAR2(100 CHAR) NOT NULL,
    MOD_DT_TM    TIMESTAMP(6) NOT NULL,
    MOD_BY       VARCHAR2(100 CHAR) NOT NULL,
    BATCH_RUN_ID NUMBER NOT NULL,
    REGION       VARCHAR2(6 CHAR),
    BNK_NO       VARCHAR2(3 CHAR) NOT NULL,
    CONSTRAINT PK_STG_HK_OBS_FACVDW PRIMARY KEY (STG_KEY)
);

CREATE SEQUENCE STG_HK_OBS_FACVDW_SEQ
    START WITH 1
    INCREMENT BY 1
    NOCACHE
    NOCYCLE;

CREATE OR REPLACE TRIGGER BI_STG_HK_OBS_FACVDW
BEFORE INSERT ON STG_HK_OBS_FACVDW
FOR EACH ROW
BEGIN
    IF :NEW.STG_KEY IS NULL THEN
        SELECT STG_HK_OBS_FACVDW_SEQ.NEXTVAL INTO :NEW.STG_KEY FROM DUAL;
    END IF;

    IF :NEW.CRTD_DT_TM IS NULL THEN
        :NEW.CRTD_DT_TM := SYSTIMESTAMP;
    END IF;

    IF :NEW.CRTD_BY IS NULL THEN
        :NEW.CRTD_BY := 'CLFACV_LOADER';
    END IF;

    IF :NEW.MOD_DT_TM IS NULL THEN
        :NEW.MOD_DT_TM := SYSTIMESTAMP;
    END IF;

    IF :NEW.MOD_BY IS NULL THEN
        :NEW.MOD_BY := 'CLFACV_LOADER';
    END IF;

    IF :NEW.BATCH_RUN_ID IS NULL THEN
        :NEW.BATCH_RUN_ID := 1;
    END IF;
END;
